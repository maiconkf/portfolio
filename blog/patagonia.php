						<h2 class="post__title--smaller">DIA 5:</h2>
						<p>De manhã cedo, saí de Puerto Natales e fui até El Calafate de ônibus. Caminhei por uns 15min até o centro da cidade e almocei no primeiro restaurante que vi aberto. Eu estava bem enjoado (eu fiquei com um pouco de náuseas na trilha de TDP), então não deu pra comer tudo. Saí de lá e fui direto para o hostel <a href="http://www.blahostel.com/" target="_blank">Bla Guesthouse</a>. O atendimento no hostel foi super bom, porém não gostei de duas coisas: 1 é que o beliche fazia barulho ao se mexer na cama e, 2, por não ter um horário limite para fazer o checkin. Na minha última noite no hostel, eu fiquei com o quarto só pra mim até a meia-noite. Depois disso, chegou um grupo de 7 pessoas no meu quarto, onde ficaram conversando e alguns ainda foram para o banho. Não foi muito legal essa noite.</p>
						<p class="post__text--margin">No fim de tarde do primeiro dia em El Calafate, eu fui até a farmácia comprar um remédio para enjôo. A mulher me vendeu um sublingual com gosto tão ruim que, ao chegar na quadra do hostel, o remédio terminou de dissolver na língua e eu vomitei na rua, do lado do hostel. Por sorte, não havia ninguém por perto. Entrei correndo no hostel e vomitei mais um pouco no banheiro. 🙃</p>
						<h2 class="post__title--smaller">DIA 6:</h2>
						<p>Dia de fazer o mini trekking no Glaciar Perito Moreno!</p>
						<p class="post__text--margin">Às 08h30min, a <a href="http://hieloyaventura.com" target="_blank">Hielo y Aventura</a> veio até meu hostel me buscar de ônibus. Haviam vários brasileiros no passeio.</p>
						<p class="post__text--margin">O meu passeio foi na seguinte ordem: transfer de barco até o glaciar, mini trekking, almoço e transfer para as passarelas. Um outro grupo começou na ordem invertida.</p>
						<p class="post__text--margin">O mini trekking dura em torno de 01h30min. Esse é o tempo de uma das experiências mais incríveis da minha vida! Antes de começar, é colocado <em>grampons</em> nos calçados e nos é ensinado a como caminhar sobre o gelo.</p>
						<div class="clearfix">
							<img src="../dist/img/blog/Grampons.jpg" alt="Grampons" class="img-responsive" width="1050" height="1050">
						</div>
						<p class="post__text--margin">No final do mini trekking, é servido whisky com o gelo do glaciar. Pra quem não bebe, é servido água e bombons.</p>
						<p class="post__text--margin">Após retirarmos os <em>grampons</em>, a gente vai almoçar (tem que levar comida) com uma das paisagens mais lindas:</p>
